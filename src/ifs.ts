import { Vec3 } from "./vector.js";

class IFS {
  fractal: number[][] = [];
  x = 0;
  y = 0;
  scaleX = 1;
  scaleY = 1;
  offsetX = 0;
  offsetY = 0;
  height: number;
  width: number;
  track: Vec3[] = [];
  constructor(f: number[], width: number = 100, height: number = 100) {
    this.width = width;
    this.height = height;
    this.readIfs(f);
    this.findBounds();
  }

  readIfs(f: number[]) {
    for (let i = 0; i < f.length; i++) f[i] = f[i] * 1;
    if (!(f.length > 0 && f.length % 7 == 0)) {
      throw new Error("Invalid IFS");
    } else {
      this.fractal = [];
      for (let i = 0; i < f.length; i += 7) {
        this.fractal.push(f.slice(i, i + 7));
      }
    }
  }

  run(n: number): Vec3[] {
    for (var i = 0; i < n; i += 1) {
      this.next();
      let px = this.scaleX * this.x + this.offsetX;
      let py = this.scaleY * this.y + this.offsetY;
      this.track.push(new Vec3(px, 0, py));
    }
    return this.track;
  }

  next() {
    let r = Math.random();
    let probabilityThreshold = 0.0;
    for (let i = 0; i < this.fractal.length; i += 1) {
      let t = this.fractal[i];
      if (r <= (probabilityThreshold += t[6])) {
        let oldx = this.x;
        this.x = t[0] * this.x + t[1] * this.y + t[4];
        this.y = t[2] * oldx + t[3] * this.y + t[5];
        return i;
      }
    }
  }

  findBounds() {
    let left = 0,
      right = 0,
      top = 0,
      bottom = 0;
    this.x = 0;
    this.y = 0;

    for (let i = 0; i < 100; i += 1) {
      this.next();
    }

    for (let i = 0; i < 10000; i += 1) {
      this.next();
      if (this.x < left) left = this.x;
      if (this.x > right) right = this.x;
      if (this.y < bottom) bottom = this.y;
      if (this.y > top) top = this.y;
    }

    if (top - bottom > right - left) {
      left = (left + right - top + bottom) / 2;
      right = left + top - bottom;
    } else {
      bottom = (bottom + top - right + left) / 2;
      top = bottom + right - left;
    }

    this.scaleX = this.width / (right - left);
    this.scaleY = this.height / (bottom - top);
    this.offsetX = (this.width * left) / (left - right);
    this.offsetY = (this.height * top) / (top - bottom);
  }
}

function create_IFS(f: number[], width: number, height: number): IFS {
  return new IFS(f, width, height);
}

